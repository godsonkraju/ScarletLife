package com.scarletlife.model;


public class Spaceship {
    int xSpaceship;
    int ySpaceship;
    Direction dirSpaceship;
    int velSpaceship;

    public Spaceship() {
    }

    public int getxSpaceship() {
        return xSpaceship;
    }

    public void setxSpaceship(int xSpaceship) {
        this.xSpaceship = xSpaceship;
    }

    public int getySpaceship() {
        return ySpaceship;
    }

    public void setySpaceship(int ySpaceship) {
        this.ySpaceship = ySpaceship;
    }

    public Direction getDirSpaceship() {
        return dirSpaceship;
    }

    public void setDirSpaceship(Direction dirSpaceship) {
        this.dirSpaceship = dirSpaceship;
    }

    public int getVelSpaceship() {
        return velSpaceship;
    }

    public void setVelSpaceship(int velSpaceship) {
        this.velSpaceship = velSpaceship;
    }
}
